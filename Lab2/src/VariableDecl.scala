

object VariableDecl {
   def main(args:Array[String])
   {
     
     //Datatype Declaration
     val name: String ="Khushi";
     println(name);
     
     var age : Int =19;
     println(age);

     //Without Datatype Declaration
     val name1 = "Kumar";
     println(name1);
     
     var age1=20; 
     println(age1);
     
   }
}


